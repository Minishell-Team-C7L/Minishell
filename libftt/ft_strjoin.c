#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*new_str;
	char	*new;
	size_t	total_size;

	if (!s1 && !s2)
		return (NULL);
	if (!s2)
		return (ft_strdup(s1));
	if (!s1)
		return (ft_strdup(s2));
	total_size = ft_strlen(s1) + ft_strlen(s2) +1;
	new_str = (char *)malloc(sizeof(char) * total_size);
	if (!new_str)
		return (NULL);
	new = new_str;
	while (*s1)
		*new_str++ = *s1++;
	while (*s2)
		*new_str++ = *s2++;
	*new_str = '\0';
	return (new);
}
