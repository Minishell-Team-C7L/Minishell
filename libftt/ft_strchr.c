#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	c1;

	c1 = c;
	while (*s)
	{
		if (*s == c1)
			return ((char *)s);
		s++;
	}
	if (c1 == '\0')
		return ((char *)s);
	return (NULL);
}
